UINT8 udm_get_symb_w (UINT32 symb)
{
    if (symb < UDM_NCHAR2_SYMBOL_START)
        return UDM_CHAR_SIZE;
    if (symb < UDM_ICON_SYMBOL_START)
        return UDM_NCHAR2_SIZE;
    if (symb < UDM_ICON2_SYMBOL_START)
        return UDM_ICON_SIZE;
    if (symb < UDM_ICON3_SYMBOL_START)
        return UDM_ICON2_SIZE;
    if (symb < UDM_PATTERN_SYMBOL_START)
        return UDM_ICON3_SIZE;
    if (symb < UDM_ICON_ARRAY_SIZE)
        return UDM_PAT_W(symb);
    return 0;
}

