/**
  * @brief This function handles USART2 global interrupt.
  */
void USART2_IRQHandler(void)
{
  /* USER CODE BEGIN USART2_IRQn 0 */
    if ((__HAL_UART_GET_FLAG(&fk_usart2,UART_FLAG_RXNE) != RESET) && 
        (__HAL_UART_GET_IT_SOURCE(&fk_usart2,UART_IT_RXNE) != RESET))
    {
        __HAL_UART_CLEAR_FLAG(&fk_usart2,UART_FLAG_RXNE);
        gfunc_fk_uart_rx_func[1]();    
    }   
    
    /* UART in mode Transmiter---------------------------------------------------*/
    if ((__HAL_UART_GET_FLAG(&fk_usart2,UART_FLAG_TXE) != RESET) &&  
        (__HAL_UART_GET_IT_SOURCE(&fk_usart2,UART_IT_TXE) != RESET))
    {
        __HAL_UART_CLEAR_FLAG(&fk_usart2,UART_FLAG_TXE);
        if (gfunc_fk_uart_tx_func[1]()==0)
        {
            FK_UART1_TX_WAIT_API();
            FK_UART2_TX_INTERRUPT_DISABLE_API(); 
            FK_UART2_LOW_SPEED_API();
        }        
    }
    
    /* UART in Over Run---------------------------------------------------*/
    if(__HAL_UART_GET_FLAG(&fk_usart1,UART_FLAG_ORE) != RESET)
    {
        __HAL_UART_CLEAR_FLAG(&fk_usart1,UART_FLAG_ORE);
    }
    
#if IGNORE_CUBEMX_CREATE == 0
  /* USER CODE END USART2_IRQn 0 */
  HAL_UART_IRQHandler(&huart2);
  /* USER CODE BEGIN USART2_IRQn 1 */
#endif
  /* USER CODE END USART2_IRQn 1 */
}

